import java.io.*;
public class Arith {
	
	public static void main(String[] args)
	{
		int a,b,sum,sub,div,mul;
		a=10;
		b=20;
        sum=a+b;
        sub=a-b;
        div=a/b;
        mul=a*b;
  
        System.out.println("The sum = "  +sum);
        System.out.println("The difference= "  +sub);
        System.out.println("The division = "  +div);
        System.out.println("The product = "  +mul);
        
	}

}
